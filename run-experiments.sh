mkdir -p workstation/maven

NUM_INSTANCES=$1
id=1
job_count=0

run_repo() {
  project="$1"
  module="${project%%_*}"
  submodule="${project#*_}"
  id="$2"
  relative_module_path="$3"
  relative_submodule_path="$4"
  group=$(basename "$(dirname "$repo_url")")
  repo=$(basename "$repo_url")
  repo_name="$group"_"$repo"
  cd workstation/maven
  if cd ${module}/${submodule}; then
    cd ..
    cp -r ${submodule} ${submodule}_under_test
    cd ../../../
    ./bash/run-experiment.sh "workstation/maven/${module}/${submodule}_under_test" "$id" "$relative_module_path" "$relative_submodule_path"
    rm -rf "workstation/maven/${module}/${submodule}_under_test"
  else
    cd ../../
    echo "[$id] Error: Failed to cd into submodule folder $module/$submodule"
    echo "[$id] Error: Failed to cd into submodule folder $module/$submodule" >>$logs_path
    exit 1
  fi
}


for ((i=0; i<${#projects[@]}; i+=3)); do
  project="${projects[i]}"
  relative_module_path="${projects[i+1]}"
  relative_submodule_path="${projects[i+2]}"
  run_repo "$project" "$id" "$relative_module_path" "$relative_submodule_path" &
  job_count=$((job_count + 1))
  id=$((id + 1))

  # Wait if max jobs are running
  if ((job_count >= NUM_INSTANCES)); then
    wait -n # wait for one job to finish
    job_count=$((job_count - 1))
  fi
done
